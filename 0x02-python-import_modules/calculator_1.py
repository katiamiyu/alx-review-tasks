#!/usr/bin/python3
""" Module contains add sub mul and div definition """

def add(a, b):
	""" addition function

	Args:
		a: first integer
		b: second integer

	Returns:
		The return value. a + b
	"""
	return (a + b)

def sub(a, b):
	""" substition function

	Args:
		a: first integer
		b: second integer

	Returns:
		The return value. a - b
	"""
	return (a - b)

def mul(a, b):
	""" multiplication function

	Args:
		a: first integer
		b: second integer

	Return:
		The return value. a * b
	"""
	return (a * b)

def div(a, b):
	""" division function

	Args:
		a: first integer
		b: second integer

	Return:
		The return value. a / b
	"""
	return int(a / b)
